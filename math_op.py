

def sum(a, b):
    return int(a) + int(b)

def sub(a, b):
    return int(a) - int(b)

def mult(a, b):
    return int(a) * int(b)

def div_rem(a, b):
    return int(a) % int(b)
    
def div_all(a, b):
    return round(int(a) / int(b), 2)
    
def div_int(a, b):
    return int(a) // int(b)

def pow(a, b):
    return int(a) ** int(b)

